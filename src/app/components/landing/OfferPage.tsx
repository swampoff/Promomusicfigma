type Tab = 'artists' | 'broadcasters';

interface SectionData {
  title: string;
  content?: { term: string; def: string }[];
  paragraphs?: string[];
}

function AccordionSection({ section, idx, isOpen, toggle }: {
  section: SectionData;
  idx: number;
  isOpen: boolean;
  toggle: () => void;
}) {
  return (
    <motion.div
      initial={{ opacity: 0, y: 16 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ delay: 0.03 * idx }}
      className="bg-white/5 backdrop-blur-sm rounded-2xl border border-white/10 overflow-hidden"
    >
      <button
        onClick={toggle}
        className="w-full flex items-center justify-between gap-4 p-5 sm:p-6 text-left hover:bg-white/5 transition-colors"
      >
        <h3 className="text-base sm:text-lg font-bold text-white">{section.title}</h3>
        {isOpen
          ? <ChevronUp className="w-5 h-5 text-slate-400 flex-shrink-0" />
          : <ChevronDown className="w-5 h-5 text-slate-400 flex-shrink-0" />}
      </button>

      <AnimatePresence>
        {isOpen && (
          <motion.div
            initial={{ height: 0, opacity: 0 }}
            animate={{ height: 'auto', opacity: 1 }}
            exit={{ height: 0, opacity: 0 }}
            transition={{ duration: 0.25 }}
            className="overflow-hidden"
          >
            <div className="px-5 sm:px-6 pb-5 sm:pb-6 space-y-3">
              {/* Definitions list */}
              {section.content && section.content.map((item, i) => (
                <div key={i} className="text-sm sm:text-base text-slate-300 leading-relaxed flex items-start gap-3">
                  <div className="w-1.5 h-1.5 rounded-full bg-[#FF577F] flex-shrink-0 mt-2" />
                  <span>
                    <strong className="text-white">{item.term}</strong> - {item.def}
                  </span>
                </div>
              ))}
              {/* Paragraphs list */}
              {section.paragraphs && section.paragraphs.map((p, i) => (
                <p key={i} className="text-sm sm:text-base text-slate-300 leading-relaxed pl-4">
                  {p}
                </p>
              ))}
            </div>
          </motion.div>
        )}
      </AnimatePresence>
    </motion.div>
  );
}

